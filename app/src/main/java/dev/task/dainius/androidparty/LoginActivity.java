    /**
     * Attempts to sign in or register the account specified by the login form.
     * If there are form errors (invalid email, missing fields, etc.), the
     * errors are presented and no actual login attempt is made.
     */
    private void attemptLogin() {

        // Reset errors.
        mUsernameView.setError(null);
        mPasswordView.setError(null);

        // Store values at the time of the login attempt.
        String username = mUsernameView.getText().toString();
        String password = mPasswordView.getText().toString();

        boolean cancel = false;
        View focusView = null;

        // Check for a valid password, if the user entered one.
        if (!TextUtils.isEmpty(password) && !isPasswordValid(password)) {
            mPasswordView.setError(getString(R.string.error_invalid_password));
            focusView = mPasswordView;
            cancel = true;
        }

        // Check for a valid email address.
        if (TextUtils.isEmpty(username)) {
            mUsernameView.setError(getString(R.string.error_field_required));
            focusView = mUsernameView;
            cancel = true;
        }

        if (cancel) {
            // There was an error; don't attempt login and focus the first
            // form field with an error.
            focusView.requestFocus();
        } else {
            // Show a progress spinner, and kick off a background task to
            // perform the user login attempt.
            showProgress(true);
            doLogin();
        }
    }

    private void doLogin() {
        RequestQueue queue = Volley.newRequestQueue(getApplicationContext());
        String url = "http://playground.tesonet.lt/v1/tokens";

        Map<String, String> data = new HashMap();
        data.put("username", mUsernameView.getText().toString());
        data.put("password", mPasswordView.getText().toString());

        JSONObject parameters = new JSONObject(data);


        JsonObjectRequest jsonObjectRequest = new JsonObjectRequest(Request.Method.POST, url, parameters, new Response.Listener<JSONObject>() {
            @Override
            public void onResponse(JSONObject response) {
                String token = null;
                try {
                    token = response.getString("token");
                } catch (JSONException e) {
                    e.printStackTrace();
                }

                Log.i("TESONET", "Request success: " + response.toString());
                Log.i("TESONET", "TOKEN: " + token);

                loadServers(token);
            }
        }, new Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError error) {
                error.printStackTrace();
                Log.i("TESONET", "Request error: " + error.getMessage());

            }
        });

        Volley.newRequestQueue(getApplicationContext()).add(jsonObjectRequest);
    }

    private void loadServers(String token) {
        DataManager.getInstance(this).setToken(token);

        Intent i = new Intent(ctx, MainActivity.class);
        ctx.startActivity(i);
    }

